package basics;

public class LogicalOperators {
	
	public static void main(String[] args) {
		
		boolean x = true;
		boolean y = false;
		
		System.out.println("x && y (AND): " + (x&&y));
		System.out.println("x || y (OR): " + (x||y));
		System.out.println("!x (NOT) : " + (!x));
	}
}
