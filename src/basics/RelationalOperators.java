package basics;

public class RelationalOperators {

	public static void main(String[] args) {
		
		int num1 = 10;
		int num2 = 3;
		int num3 = 7;
		
		System.out.println("num 1 < num 2: " + (num1 < num2));
		System.out.println("num 1 > num 2: " + (num1 > num2));
		System.out.println("num 1 <= num 2: " + (num1 <= num2));
		System.out.println("num 1 >= num 2: " + (num1 >= num2));
		System.out.println("num 1 == num 2: " + (num1 == num2));
		System.out.println("num 1 != num 2: " + (num1 != num2));
		
	}
	
}
